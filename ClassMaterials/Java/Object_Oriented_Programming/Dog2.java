package ClassMaterials.Java.Object_Oriented_Programming;

public class Dog2 {
    private String breed;
    private int age;

    public Dog2(String breed, int age) {
        this.breed = breed;
        this.age = age;
    }

    public String getBreed() {
        return breed;
    }

    public void setBreed(String breed) {
        this.breed = breed;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
